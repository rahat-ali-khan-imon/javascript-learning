// Print simple text
console.log('Hello World!');


// Print another string
console.log('I love programming.');


// Print integer number
console.log(123454);


// Print floating / decimal number
console.log(60.55);


// String Concatenation
// number 9 automatically converts to string
console.log('My fav no: ' + 9);


// Number addition
console.log(9 + 9); // 18


// String + number
// number converts into string
console.log('9' + 9); // 99


// Store concatenated string into variable
let s = 'My fav no: ' + 9;


// Print value and its data type
console.log(s, '--->', typeof s);
// Output: My fav no: 9 ---> string


// Modern JavaScript Template Literal
let modern_js = `My fav no: ${9}`;

console.log(modern_js, '--->', typeof modern_js);
// Output: My fav no: 9 ---> string