// Arithmetic Operator

let a = 10;
let b = 3;

console.log(a % b);
console.log(a % 6);
console.log();


/** Increment & Decrement
 * Pre Increment
 * Post Increment
*/

console.log(a++);   // Post Increment
console.log(a);
console.log(++a);  // Pre Increment
console.log(a);
console.log();


console.log(a--);   // Post Decrement
console.log(a);
console.log(--a);   // Pre Decrement
console.log(a);