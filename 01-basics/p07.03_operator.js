// Comparison Operators

let a = 10;
let b = 20;

console.log(a == b);    // false
console.log(a != b);   // true
console.log();

console.log(a > b);     // false
console.log(a < b);    // true
console.log();


a = 20;

console.log(a >= b);    // true 
console.log(a <= b);   // true
console.log();


let c = '50';
let d = 50;

console.log(c == d);        // true 
console.log(c === d);      // false